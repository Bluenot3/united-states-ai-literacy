create or replace function public.is_zen_vanguard_admin()
returns boolean
language sql
stable
set search_path = public
as $$
    select
        coalesce(((select auth.jwt()) -> 'app_metadata' ->> 'role') = 'admin', false)
        or lower(coalesce((select auth.jwt()) ->> 'email', '')) in (
            'royaltokens@gmail.com',
            'huxley@zenai.biz'
        );
$$;

